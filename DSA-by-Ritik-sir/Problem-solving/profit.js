// Q Take the cost price and selling price of a product, and print the profit obtained on the product.

// Input 1
// ```
// 50 100
// ```

// Output 1:
// ```
// 50
// ```
// Explanation 1:

// ```
// 100 - 50 = 50
// ```

function profit(costPrice, sellingPrice) {
  let a = costPrice;
  let b = sellingPrice;
  z = b - a;
  return z;
}
console.log(profit(50, 100));
console.log(profit(30, 20));

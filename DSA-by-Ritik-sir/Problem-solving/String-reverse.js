// Q Given a string, return the string in reverse

// Input:
// ```
// aircampus
// ```
// Output:
// ```
// supmacria
// ```
// Input:
// ```
// javascript
// ```
// Output:
// ```
// tpircsavaj
// ```

function reverse(str){
  
  
    return str.split("").reverse().join("")
  }
  console.log(reverse('aircampus'))
  console.log(reverse('javascript'))
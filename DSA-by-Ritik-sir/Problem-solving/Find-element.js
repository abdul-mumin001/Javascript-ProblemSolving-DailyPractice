// Q Create an Array with the following elements: `“Bike”, “Scooter”, “Car”, “Truck”, “JCB” , “Helicopter”`
// and return the 4th element from the Array.

// ### Output Format
// 4th Element from the Array

// ### Sample Output :
// `
